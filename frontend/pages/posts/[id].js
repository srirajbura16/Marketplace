import styles from '../../styles/AdDetails.module.css';
import Head from 'next/head';
import Link from 'next/link';
import API_URL from '../../lib/API_URL';

export default function AdDetails({ data: ad }) {
  const { title, description, price, condition, image_url, user } = ad;
  const { username, email } = user;
  return (
    <div className="container">
      <Head>
        <title>{title}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className={styles.ad}>
        <h1>{title}</h1>
        <hr />
        <div className={styles.flex}>
          <div className={styles.ad_image}>
            <img src={image_url} alt="ad image" />
          </div>
          <div className={styles.ad_info}>
            <div>
              <div className="price">$ {price}</div>
              <div className={styles.condition}>{condition}</div>
            </div>
            <div className={styles.description}>
              <h1>Description</h1>
              <hr />
              {description}
            </div>
          </div>
        </div>
        <h1 className="contact">Contact</h1>
        <hr />
        <div className={styles.user}>
          <div>{username}</div>
          <div>{email}</div>
        </div>
      </div>
    </div>
  );
}

export async function getServerSideProps(context) {
  const { id } = context.params;
  const res = await fetch(`${API_URL}/api/ads/${id}`);
  const data = await res.json();

  if (!data) {
    return {
      notFound: true,
    };
  }

  return {
    props: { data },
  };
}
